# Tamsayıyı ondalıklı sayıya çevirme.
a = 43
a = float(a)
print(a)
print(float(-4))

# Ondalıklı sayıyı tam sayıya çevirme.
b = 4.5
b = int(b)
print(b)
print(int(5))

# Sayıları stringe çevirme.
c = "3.145423"
print(len(c))
d = "5432"

# Stringleri tamsayıya çevirme.
print(int(d))

# Stringleri ondalıklı sayıya çevirme.
