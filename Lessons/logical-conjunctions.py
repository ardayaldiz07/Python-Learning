a = "Arda"
b = "Arda"

# Ve operatörü genel sonucun True olması için bütün işlemlerin doğru olması gerek.
print(1 > 0 and a == b)
# Veya operatörü genel sonucun True olması için tek bir işlemin doğru olması yeter.
print(1 > 0 or a == b)

# Not operatörü değeri tersine çevirir.

print(not 2 == 2)
print("Canberk" < "Oğuz")
